            <img src="image/Baklava.jpg" alt="Baklava" style="width:300px; height:auto">
            <p>Baklava is a rich, sweet pastry made of layers of filo dough filled with chopped nuts and sweetened with honey or syrup. It is a popular dessert in Middle Eastern and Mediterranean cuisines.</p>
            <ul>
                <li><strong>Preparation Time:</strong> 30 minutes</li>
                <li><strong>Cooking Time:</strong> 45 minutes</li> 
                <li><strong>Total Time:</strong> 1 hour 15 minutes</li>
                <li><strong>Servings:</strong> 12</li>
                <li><strong>Nutrition Facts (per serving):</strong> 300 calories, 5g protein, 35g carbs, 18g fat</li>
            </ul>
        
            <h3>Ingredients:</h3>
            <ul>
                <li>1 package of filo dough</li>
                <li>2 cups chopped nuts (walnuts, pistachios, or almonds)</li>
                <li>1 cup melted butter</li>
                <li>1 cup honey</li>
                <li>1 cup water</li>
                <li>1 teaspoon vanilla extract</li>
                <li>1 teaspoon ground cinnamon</li>
            </ul>
            <h3>Directions:</h3>
            <ol>
                <li>Preheat the oven to 350°F (175°C).</li>
                <li>Layer half of the filo dough in a greased baking dish, brushing each layer with melted butter.</li>
                <li>Mix the chopped nuts with cinnamon and sprinkle over the filo layers.</li>
                <li>Add the remaining filo layers on top, again brushing each layer with butter.</li>
                <li>Cut the baklava into diamond shapes before baking.</li>
                <li>Bake for about 45 minutes until golden brown.</li>
                <li>In a saucepan, combine honey, water, and vanilla. Bring to a boil and simmer for 10 minutes.</li>
                <li>Pour the syrup over the hot baklava as soon as it comes out of the oven. Let it cool before serving.</li>
            </ol>
