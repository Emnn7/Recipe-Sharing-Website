            <p><strong>Step 3</strong> Place peppercorns, cloves, dried ginger, cumin seeds, thyme, marjoram, and oregano in a mortar and grind into a fine mix.
            <p><strong>Step 4</strong> In a blender, combine soaked chilies, 1/4 cup of the chile soaking liquid, onion-tomato mix, ground spices, chicken bouillon, and apple cider vinegar. Blend until smooth. Add a small amount of beef broth if needed for blending. Taste sauce; season with salt.</p>
            <p><strong>Step 5</strong> To cook meat, place chuck roast, beef shanks, and beef short ribs in a large pot and pour the sauce over it. Add enough beef broth to cover meat by about 1 inch, then stir to combine. Bring to a boil over high heat, then add cinnamon stick, onion, garlic, and bay leaves. Cover, and reduce heat to low, and simmer until meat is tender and falling apart, about 2 1/2 hours. Remove and discard short rib bones; skim fat from surface of broth using a spoon. Reserve fat for another use, such as quesabirrias. Taste broth; season with salt.
           </p>
           <p><strong>Step 6</strong> Bring a large pot of salted water to a boil, add ramen, and cook until tender, 7 to 20 minutes, or according to package instructions. Drain; divide noodles into bowls.</p>
           <p><strong>Step 7</strong> To assemble the dish, ladle birria and broth over ramen. Top with chopped onion and cilantro. Serve with lime wedges.</p>
